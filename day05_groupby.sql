-- =====================================================
-- PART 1: 집계함수 기초
-- =====================================================

-- ─────────────────────────────────────────────────
-- COUNT(*) vs COUNT(컬럼) 차이
--   COUNT(*)   : NULL 포함 모든 행 카운트
--   COUNT(컬럼): 해당 컬럼 값이 NULL이 아닌 행만 카운트
-- ─────────────────────────────────────────────────

-- orders 테이블 전체 행 수
SELECT COUNT(*) FROM orders;

-- customer_id가 NULL인 행은 없으므로 결과 동일
SELECT COUNT(customer_id) FROM orders;

-- customers 테이블에서 차이 확인
-- (email을 입력하지 않은 고객은 NULL → COUNT(email)이 더 작게 나옴)
SELECT COUNT(*) AS 전체고객수, COUNT(email) AS 이메일등록수 FROM customers;

-- ─────────────────────────────────────────────────
-- SUM, AVG, MAX, MIN
-- ─────────────────────────────────────────────────

-- 전체 총 매출
SELECT SUM(total_price) AS 총매출 FROM orders;

-- 평균 주문금액 (소수점 포함)
SELECT AVG(total_price) AS 평균주문금액 FROM orders;

-- 최고/최저 주문금액
SELECT MAX(total_price) AS 최고주문금액, MIN(total_price) AS 최저주문금액 FROM orders;

-- ROUND로 반올림한 평균 주문금액
SELECT ROUND(AVG(total_price)) AS 평균주문금액 FROM orders;

-- 전체 배달건수, 배달료 합계, 평균 배달료
SELECT
  COUNT(*)                    AS 총주문건수,
  SUM(delivery_fee)           AS 총배달료,
  ROUND(AVG(delivery_fee))    AS 평균배달료
FROM orders;

-- =====================================================
-- PART 2: GROUP BY 기초
-- =====================================================

-- ─────────────────────────────────────────────────
-- 가게별 주문 수
-- ─────────────────────────────────────────────────
SELECT
  restaurant_id,
  COUNT(*) AS 주문수
FROM orders
GROUP BY restaurant_id;

-- ─────────────────────────────────────────────────
-- 가게별 총 매출 (높은 순 정렬)
-- ─────────────────────────────────────────────────
SELECT
  restaurant_id,
  SUM(total_price) AS 총매출
FROM orders
GROUP BY restaurant_id
ORDER BY 총매출 DESC;

-- ─────────────────────────────────────────────────
-- 주문 상태별 건수 (status 컬럼 그룹핑)
-- ─────────────────────────────────────────────────
SELECT
  status,
  COUNT(*) AS 건수
FROM orders
GROUP BY status;

-- ─────────────────────────────────────────────────
-- 날짜 함수 조합: 월별 주문수
--   DATE_FORMAT(날짜컬럼, '%Y-%m') → '2024-11' 형태로 변환
-- ─────────────────────────────────────────────────
SELECT
  DATE_FORMAT(order_date, '%Y-%m') AS 월,
  COUNT(*) AS 주문수
FROM orders
GROUP BY 월
ORDER BY 월;

-- 월별 총 매출도 함께 확인
SELECT
  DATE_FORMAT(order_date, '%Y-%m') AS 월,
  COUNT(*)             AS 주문수,
  SUM(total_price)     AS 총매출,
  ROUND(AVG(total_price)) AS 평균주문금액
FROM orders
GROUP BY 월
ORDER BY 월;

-- =====================================================
-- PART 3: HAVING
-- =====================================================

-- ─────────────────────────────────────────────────
-- WHERE vs HAVING 차이
--   WHERE  : GROUP BY 이전에 개별 행 필터링 (집계 전)
--   HAVING : GROUP BY 이후에 그룹 필터링 (집계 후)
--   → 집계함수(COUNT, SUM, AVG 등)는 HAVING에서만 사용 가능
-- ─────────────────────────────────────────────────

-- 주문이 5건 이상인 가게
SELECT
  restaurant_id,
  COUNT(*) AS 주문수
FROM orders
GROUP BY restaurant_id
HAVING 주문수 >= 5;

-- 평균 주문금액 15,000원 이상인 가게
SELECT
  restaurant_id,
  ROUND(AVG(total_price)) AS 평균금액
FROM orders
GROUP BY restaurant_id
HAVING 평균금액 >= 15000;

-- ─────────────────────────────────────────────────
-- 올바른 예 vs 잘못된 예
-- ─────────────────────────────────────────────────

-- [잘못된 예] 집계 전 필터(배달완료 건만)를 HAVING에 쓰면 오류 가능
-- ❌ 아래처럼 쓰지 말 것
-- SELECT restaurant_id, COUNT(*) AS 주문수
-- FROM orders
-- GROUP BY restaurant_id
-- HAVING status = '배달완료';   -- status는 집계 전 컬럼 → WHERE로 써야 함

-- [올바른 예] 배달완료 건만 → WHERE로 먼저 필터 후 GROUP BY
SELECT
  restaurant_id,
  COUNT(*) AS 배달완료수
FROM orders
WHERE status = '배달완료'           -- 집계 전 개별 행 필터
GROUP BY restaurant_id
HAVING 배달완료수 >= 5;             -- 집계 후 그룹 필터

-- 고객별 총 지출 50,000원 이상
SELECT
  customer_id,
  SUM(total_price) AS 총지출
FROM orders
GROUP BY customer_id
HAVING 총지출 >= 50000
ORDER BY 총지출 DESC;

-- =====================================================
-- PART 4: 다중 GROUP BY + 실전 쿼리
-- =====================================================

-- ─────────────────────────────────────────────────
-- 월 + 가게별 그룹핑
-- ─────────────────────────────────────────────────
SELECT
  DATE_FORMAT(order_date, '%Y-%m') AS 월,
  restaurant_id,
  COUNT(*)         AS 주문수,
  SUM(total_price) AS 매출
FROM orders
GROUP BY 월, restaurant_id
ORDER BY 월, 매출 DESC;

-- ─────────────────────────────────────────────────
-- 고객별 총 주문금액, 주문횟수
-- ─────────────────────────────────────────────────
SELECT
  customer_id,
  COUNT(*)         AS 주문횟수,
  SUM(total_price) AS 총지출
FROM orders
GROUP BY customer_id
ORDER BY 총지출 DESC;

-- ─────────────────────────────────────────────────
-- 가게별 + 주문 수량별 분포 확인
-- ─────────────────────────────────────────────────
SELECT
  restaurant_id,
  quantity,
  COUNT(*) AS 건수
FROM orders
GROUP BY restaurant_id, quantity
ORDER BY restaurant_id, quantity;

-- =====================================================
-- PART 5: 실전 시나리오 쿼리 (사장님이 알고 싶어합니다)
-- =====================================================

-- ─────────────────────────────────────────────────
-- [시나리오 1]
-- 사장님이 알고 싶어합니다:
-- "우리 서비스 전체 매출이 얼마야? 총 몇 건 주문이 들어왔어?"
-- ─────────────────────────────────────────────────
SELECT
  COUNT(*)             AS 총주문건수,
  SUM(total_price)     AS 총매출,
  ROUND(AVG(total_price)) AS 평균주문금액,
  MIN(total_price)     AS 최소주문금액,
  MAX(total_price)     AS 최대주문금액
FROM orders;

-- ─────────────────────────────────────────────────
-- [시나리오 2]
-- 사장님이 알고 싶어합니다:
-- "가게별로 얼마나 팔았어? 매출 상위 5개 가게 알고 싶어."
-- ─────────────────────────────────────────────────
SELECT
  restaurant_id,
  COUNT(*)             AS 주문건수,
  SUM(total_price)     AS 총매출,
  ROUND(AVG(total_price)) AS 평균주문금액
FROM orders
GROUP BY restaurant_id
ORDER BY 총매출 DESC
LIMIT 5;

-- ─────────────────────────────────────────────────
-- [시나리오 3]
-- 운영팀이 알고 싶어합니다:
-- "월별로 주문량이 어떻게 변하고 있어? 성수기가 언제야?"
-- ─────────────────────────────────────────────────
SELECT
  DATE_FORMAT(order_date, '%Y-%m')   AS 월,
  COUNT(*)                            AS 주문건수,
  SUM(total_price)                    AS 월매출,
  ROUND(AVG(total_price))             AS 평균주문금액
FROM orders
GROUP BY 월
ORDER BY 월;

-- ─────────────────────────────────────────────────
-- [시나리오 4]
-- 마케팅팀이 알고 싶어합니다:
-- "우리 단골 고객 TOP 5 알려줘. 지출 많은 순으로."
-- ─────────────────────────────────────────────────
SELECT
  customer_id,
  COUNT(*)             AS 주문횟수,
  SUM(total_price)     AS 총지출,
  ROUND(AVG(total_price)) AS 평균주문금액,
  MAX(order_date)      AS 최근주문일
FROM orders
GROUP BY customer_id
ORDER BY 총지출 DESC
LIMIT 5;

-- ─────────────────────────────────────────────────
-- [시나리오 5]
-- 운영팀이 알고 싶어합니다:
-- "주문이 뜸한 가게(월 3건 미만)가 어디야? 관리가 필요해 보여."
-- ─────────────────────────────────────────────────
SELECT
  DATE_FORMAT(order_date, '%Y-%m') AS 월,
  restaurant_id,
  COUNT(*)                          AS 월별주문수
FROM orders
GROUP BY 월, restaurant_id
HAVING 월별주문수 < 3
ORDER BY 월, 월별주문수;
